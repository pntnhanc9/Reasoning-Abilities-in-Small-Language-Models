@dataclass
class InferenceResult:
    """Stores the results from a single sample/particle."""

    # Required
    pipeline_name: str
    task: Task
    N: int
    sampling_type: str

    # Attempt information
    attempt: int = 1
    is_final_attempt: bool = True

    # Optional (if inference was successful)
    text: str = None  # Answer text generated by the model
    full_text: str = None  # Full text including reasoning, if applicable
    sample_i: int = None
    weight: float = None
    finished: bool = None
    prompt: str = None
    check_result: bool = None  # Result of the model's check() method
    evaluate_results: Dict[str, bool] = (
        None  # Results of the dataset's evaluate() method
    )

    # Token counts
    prompt_tokens: int = None
    completion_tokens: int = None
    reasoning_tokens: int = None  # reasoning_tokens <= completion_tokens

    # Error info (optional)
    error_source: str = None
    error: str = None
    traceback: str = None


class SamplingType(StrEnum):
    INDEPENDENT = "independent"  # Draw N samples independently. Allows drawing max(N) samples and computing results retrospectively.
    ADAPTIVE = "adaptive"  # Draw N samples with potential interactions between samples. Requires running each value of N separately.
    SINGLE_SHOT = "single_shot"  # Only draw N=1 samples.


class InferencePipeline(ABC):
    def __init__(self, results_dir: str, **kwargs):
        self.results_dir = results_dir

    def __repr__(self):
        return f"{self.__class__.__name__}(results_dir={self.results_dir})"

    def __str__(self):
        return self.__repr__()

    @property
    def name(self):
        pass

    @property
    def sampling_type(self):
        return SamplingType.INDEPENDENT

    @property
    def cot(self):
        """Chain of Thought (CoT) reasoning."""
        return getattr(self, "_cot", False)

    @cot.setter
    def cot(self, value):
        """Set Chain of Thought (CoT) reasoning."""
        self._cot = value

    @property
    def system_prompt(self):
        if self.enable_manual_cot:
            return (
                "You are helping a user generate text that satisfies constraints. "
                "Think step-by-step in order to follow the user's instructions. "
                "Your final response should be enclosed in <answer></answer> tags."
            )
        else:
            return (
                "You are helping a user generate text that satisfies constraints. "
                "Follow the user's instructions exactly. Write your response below; "
                "do not preface your response or include any additional remarks."
            )

    @property
    def default_sent_prompt(self):
        return "Write a sentence that is grammatically correct and makes sense."

    def get_messages_for_task(self, task: Task, system_role: str = "system"):
        """Get chat messages for the task."""
        messages = []
        if system_role is not None:
            messages += [
                {
                    "role": system_role,
                    "content": self.system_prompt,
                }
            ]
        messages += [{"role": "user", "content": task.prompt}]
        if system_role is not None and self.enable_manual_cot:
            messages += [
                {
                    "role": system_role,
                    "content": (
                        "REMEMBER: Your final response should be enclosed in <answer></answer> tags."
                    ),
                }
            ]
        return messages

    def parse_answer(self, text: str) -> str:
        """Parse the answer from the text. If CoT is enabled, extract the answer from <answer></answer> tags. Otherwise, return the text as is.

        If the text does not contain <answer></answer> tags, return the text as is.
        """
        if self.enable_manual_cot:
            start_tag = "<answer>"
            end_tag = "</answer>"
            start_idx = text.find(start_tag)
            end_idx = text.find(end_tag, start_idx + len(start_tag))
            if start_idx != -1 and end_idx != -1:
                return text[start_idx + len(start_tag) : end_idx]
        return text

    async def generate_model(self, task: Task, **kwargs) -> Optional[BaseModel]:
        """Generate an InferenceModel class for a task. Intentional no-op for baselines."""
        pass

    @abstractmethod
    async def infer(self, task: Task, N: int, **kwargs) -> List[InferenceResult]:
        """Run inference on a task."""
        pass

    async def __call__(
        self, task: Task, N: Union[int, List[int]], **kwargs
    ) -> List[InferenceResult]:
        if isinstance(N, int):
            N = [N]

        results_list = []

        if self.sampling_type == SamplingType.SINGLE_SHOT:
            N = [1]
        elif self.sampling_type == SamplingType.INDEPENDENT:
            N = [max(N)]
        elif self.sampling_type == SamplingType.ADAPTIVE:
            pass
        else:
            raise ValueError(f"Unknown sampling type: {self.sampling_type}")

        for n in N:
            results_n = await self.infer(task, n, **kwargs)
            assert len(results_n) == n
            results_list += results_n

        assert len(results_list) == sum(N)
        return results_list


pipeline_registry = {}


def register_pipeline(name):
    def decorator(cls):
        cls.name = name
        pipeline_registry[name] = cls
        return cls

    return decorator


def pipeline_factory(config: Dict) -> InferencePipeline:
    pipeline_type = config["type"]
    params = config.get("params", {})

    if pipeline_type in pipeline_registry:
        return pipeline_registry[pipeline_type](**params)
    else:
        raise ValueError(f"Unknown pipeline type: {pipeline_type}")


def load_pipelines_from_configs(
    config_files: List[str], results_dir: str, params: Dict = {}
) -> List[InferencePipeline]:
    """Load pipelines from a list of config files.

    Args:
        config_files: List of config file names (without the ".json" extension).
        results_dir: Directory to store results. Each pipeline will have its own subdirectory.
        params: Additional parameters to pass to the pipelines.
            Overwrites any existing values for these params in the config files.

    """
    pipeline_list = []
    for config_file in config_files:
        config_path = os.path.join(
            os.path.dirname(__file__), "configs", f"{config_file}.json"
        )
        with open(config_path, "r") as f:
            config = json.load(f)
        _params = dict(params)
        _params["results_dir"] = os.path.join(
            results_dir, PIPELINES_DIR, config.get("name", config["type"])
        )

        # Avoid overwriting with None values
        def deep_update(orig, update):
            for key, value in update.items():
                if value is None:
                    continue
                if isinstance(value, dict) and isinstance(orig.get(key), dict):
                    deep_update(orig[key], value)
                else:
                    orig[key] = value

        deep_update(config["params"], _params)

        pipeline = pipeline_factory(config)

        # update pipeline name from config
        if "name" in config:
            pipeline.name = config["name"]

        # Ensure unique pipeline names
        if pipeline.name in set([p.name for p in pipeline_list]):
            raise ValueError(f"Duplicate pipeline name detected: {pipeline.name}")

        pipeline_list.append(pipeline)

    return pipeline_list


class SMCInferencePipeline(InferencePipeline):
    def __init__(self, results_dir: str, **kwargs):
        super().__init__(results_dir)

    @property
    def sampling_type(self):
        return SamplingType.ADAPTIVE

    async def infer(
        self,
        task: Task,
        N: int,
        model_cls: BaseModel,
        lm: llamppl.CachedCausalLM,
        use_proposal: bool = True,
        max_tokens: int = 32,
        temperature: float = 1.0,
        ess_threshold: float = 0.5,
        timeout: float = 120.0,
        max_steps: int = 1000,
        cache_kv: bool = True,
        visualize: bool = False,
        **kwargs,
    ) -> List[InferenceResult]:
        """Run SMC inference with the model_cls."""
        assert isinstance(N, int)
        lm.batch_size = N

        try:
            model = await model_cls.create(
                lm=lm,
                task_prompt=task.prompt,
                max_tokens=max_tokens,
                temperature=temperature,
            )
        except AsyncEngineDeadError as e:
            raise e
        except Exception as e:
            logger.error(f"Model initialization failed")
            return [
                InferenceResult(
                    pipeline_name=self.name,
                    task=task,
                    N=N,
                    sampling_type=str(self.sampling_type),
                    error_source="model_init",
                    error=str(e),
                    traceback=traceback.format_exc(),
                )
            ] * N

        logger.info(f"Running SMC inference ({N} particles, timeout={timeout}s)")
        try:
            # Context manager requires Python 3.11+
            async with asyncio.timeout(timeout):
                particles = await smc_standard(
                    model,
                    n_particles=N,
                    ess_threshold=ess_threshold,
                    max_steps=max_steps,
                    visualize=visualize,
                )
        except AsyncEngineDeadError as e:
            raise e
        except (asyncio.TimeoutError, TimeoutError) as e:
            lm.reset_async_queries()
            return [
                InferenceResult(
                    pipeline_name=self.name,
                    task=task,
                    N=N,
                    sampling_type=str(self.sampling_type),
                    error_source="smc_standard",
                    error=f"TimeoutError(timeout={timeout})",
                    traceback=f"Inference timed out after {timeout} seconds. Check the InferenceModel to ensure loop termination.",
                )
            ] * N
        except Exception as e:
            lm.reset_async_queries()
            return [
                InferenceResult(
                    pipeline_name=self.name,
                    task=task,
                    N=N,
                    sampling_type=str(self.sampling_type),
                    error_source="smc_standard",
                    error=str(e),
                    traceback=traceback.format_exc(),
                )
            ] * N

        # Check to ensure all particles are done stepping
        if not all(p.done_stepping() for p in particles):
            lm.reset_async_queries()
            return [
                InferenceResult(
                    pipeline_name=self.name,
                    task=task,
                    N=N,
                    sampling_type=str(self.sampling_type),
                    error_source="smc_standard",
                    error=f"Max inference steps exceeded (t={max_steps})",
                    traceback=f"Max steps inference exceeded (t={max_steps}). Check the InferenceModel to ensure it includes self.finish() and loops are properly terminated.",
                )
            ] * N

        def _convert_weight_to_float(weight):
            """Convert weight to float, handling various types.

            NOTE: This is a corner case that occurs with Qwen3 models and needs debugging.
            """
            try:
                return float(weight)
            except:
                pass

            logger.warning(
                f"Unable to convert weight to float: type {type(weight)}, value: {weight}"
            )
            if isinstance(weight, np.ndarray):
                if weight.size == 0:
                    logger.warning("Particle weight is empty array, returning -inf.")
                    return float("-inf")
                elif weight.size == 1:
                    return float(weight.item())

            raise ValueError(f"Unexpected weight: type {type(weight)}, value: {weight}")

        return [
            InferenceResult(
                pipeline_name=self.name,
                task=task,
                N=N,
                sampling_type=str(self.sampling_type),
                text=str(p),
                completion_tokens=p.context.token_count,
                prompt_tokens=p.context.prompt_token_count,
                sample_i=i,
                weight=_convert_weight_to_float(p.weight),
                finished=p.finished,
                prompt=task.prompt,
                check_result=p.check_result,
                evaluate_results=task.evaluate(str(p)),
            )
            for i, p in enumerate(particles)
        ]


